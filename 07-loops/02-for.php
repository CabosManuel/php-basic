<?php

for ($i = 0; $i < 10; $i++){
	echo "*";
	echo "\n";
}

# for con DOBLE CONTADOR --------------------------------------
for ($i = 0, $j = 10; $i < 10, $j > 0; $i++, $j--){
	$a = 0;
	$b = 0;

	while ($a < $i) {
		echo "*  ";
		$a++;
	}
	
	while ($b < $j) {
		echo "+  ";
		$b++;
	}

	echo "\n";
}

